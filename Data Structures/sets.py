first = {1,2,3,4}
second = {1,5,6,7}

union = first | second
print(f"Union: {union}")

intersection = first & second
print(f"Intersection: {intersection}")

difference = first - second
print(f"Difference: {difference}")

symmetrical_diff = first ^ second
print(f"Symmetrical Difference: {symmetrical_diff}")


